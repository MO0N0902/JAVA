package twoArray;

public class TwoArrayTest01 {
	public static void main(String[] args) {
		int[][] ar1; // 2차원 배열 선언
		ar1 = new int[3][2]; // 3행 2열 크기의 배열 생성
		System.out.println(ar1); //3행 2열의 참조값
		//2차원 배열 전체의 참조값
		System.out.println(ar1[0]);
		//0행의 참조값
		System.out.println(ar1[0][0]); //0행 0열의 값
		//0행 0열의 값
		
		
	}
}
